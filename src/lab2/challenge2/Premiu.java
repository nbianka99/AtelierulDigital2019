package lab2.challenge2;

public class Premiu {
    private String name;
    private int an;

    public Premiu(String name, int an){
        this.name=name;
        this.an=an;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAn() {
        return an;
    }

    public void setAn(int an) {
        this.an = an;
    }
}
